export function getStorefrontFilter(item) {
  if (item.slug === 'birthday') return { key: 'occasion', value: 'Birthday' }
  if (item.slug === 'anniversary') return { key: 'occasion', value: 'Anniversary' }
  return { key: 'category', value: item.label }
}

export function getStorefrontHref(item) {
  const { key, value } = getStorefrontFilter(item)
  return `/menu?${key}=${encodeURIComponent(value)}`
}